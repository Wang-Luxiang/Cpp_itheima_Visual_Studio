#include <iostream>

using namespace std;

/*
 * Escape Character
 * \\   Slash
 * \n   New line
 * \t   Tab
 */

int main() {
    cout << "==================================================" << endl;
    cout << "a \n a" << endl;
    cout << "==================================================" << endl;

    cout << endl;

    cout << "==================================================" << endl;
    cout << "a \t a" << endl;
    cout << "==================================================" << endl;

    cout << endl;

    cout << "==================================================" << endl;
    cout << "a \\ a" << endl;
    cout << "==================================================" << endl;

    cout << endl;

    return 0;
}
