/**
 * for (initial expression; condition expression; End loop) {
 *      loop statement
 * }
 */

#include <iostream>
using namespace std;

int main() {
    // for loop
    // Output 0 to 9
    for (int i = 0; i < 10; i++) {
        cout << i << endl;
    }
    return 0;
}
